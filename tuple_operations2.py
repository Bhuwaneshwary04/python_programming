numbers = (4, 7, 4, 9, 2, 7, 6, 4, 5, 9)

#Count the number of occurrences of 4
count_of_4 = numbers.count(4)
print("Number of occurrences of 4:", count_of_4)

#Find the index of a specific number (9)
index_of_9 = numbers.index(9)
print("Index of 9:", index_of_9)

#Find the maximum and minimum numbers in the tuple
max_number = max(numbers)
min_number = min(numbers)
print("Maximum number:", max_number)
print("Minimum number:", min_number)

#Calculate the total (sum) of all elements
total_of_elements = sum(numbers)
print("Total of all elements:", total_of_elements)

#Find the total number of elements in the tuple
total_elements = len(numbers)
print("Total number of elements:", total_elements)
