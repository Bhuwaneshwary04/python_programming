#Creating a set with some elements
new_set = {10, 20, 30, 40, 50}

#Printing the created set
print("The created set is:", new_set)

#Creating an empty set (note: {} creates a dictionary, not a set)
empty_set = set()
print("An empty set:", empty_set)

#Creating a set with mixed data types
mixed_set = {10, "Hello", 3.14, True}
print("A set with mixed data types:", mixed_set)
