#Create a tuple with mixed data types
mixed_tuple = (10, "Hello", 3.14, True, [1, 2, 3], (5, 6))

#Print the tuple
print("Tuple with different data types:", mixed_tuple)

#Accessing elements from the tuple
print("First element (int):", mixed_tuple[0])
print("Second element (string):", mixed_tuple[1])
print("Third element (float):", mixed_tuple[2])
print("Fourth element (boolean):", mixed_tuple[3])
print("Fifth element (list):", mixed_tuple[4])
print("Sixth element (tuple):", mixed_tuple[5])
