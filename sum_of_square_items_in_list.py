# Example list 
my_list = [1, 2, 3, 4] 

# Sum of squares of all items
sum_squares = 0
for item in my_list: 
    sum_squares += item ** 2

 # Multiply all items in the list
product = 1
for item in my_list:
     product *= item 
print("Sum of squares:", sum_squares) 
print("Product of all items:", product)
